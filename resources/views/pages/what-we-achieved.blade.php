        <span class="small-title">YumPiper</span>
        Based on machine learning a browser plugin was developed,
        that takes recipes and classifies them based on the yumminess and healthiness.


        <span class="small-title">Chubby Joe and Fat Matilda</span>
        Similar to tobacobody,
        this team developed a website that depicts the consequences of overweight to educate people about obesity.

        <span class="small-title">Speech Therapy Games</span>
        To support children in their speech correction,
        this team developed a game that helps them with the pronunciation of words,
        syllables and the creation of noises.


        <span class="small-title">Sigmund Frog</span>
        Children’s waiting rooms are boring and could be way more exciting!
        With the help of an XBOX and Kinect,
        children are educated in a game like manner how to brush their teeth.
        In order that their parents don’t get bored that game has also been brushed up by including lessons for them.

        <span class="small-title">CME Community</span>
        Education of healthcare professionals such as doctors is quite boring and inefficient.
        To change that project CME has been created.
        By applying gamification and teaching with quizzes, riddles,
        videos and photos this experience should become more comfortable and efficient.

